def expand_synonyms(query: str) -> str:
    """
    –†–∞—Å—à–∏—Ä—è–µ—Ç –∞–±–±—Ä–µ–≤–∏–∞—Ç—É—Ä—ã –∏ —Å–∏–Ω–æ–Ω–∏–º—ã –≤ –∑–∞–ø—Ä–æ—Å–µ
    """
    query_lower = query.lower().strip()
    
    # –ü—Ä—è–º–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ –≤ —Å–ª–æ–≤–∞—Ä–µ
    if query_lower in SYNONYM_DICT:
        return SYNONYM_DICT[query_lower]
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–ª—é—á–∏ –æ—Ç —Å–∞–º—ã—Ö –¥–ª–∏–Ω–Ω—ã—Ö –∫ —Å–∞–º—ã–º –∫–æ—Ä–æ—Ç–∫–∏–º –¥–ª—è –∏–∑–±–µ–∂–∞–Ω–∏—è –ª–æ–∂–Ω—ã—Ö —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π
    sorted_keys = sorted(SYNONYM_DICT.keys(), key=len, reverse=True)
    
    # –ó–∞–º–µ–Ω–∞ –≤—Ö–æ–∂–¥–µ–Ω–∏–π
    for key in sorted_keys:
        if key in query_lower:
            # –ó–∞–º–µ–Ω—è–µ–º —Å —É—á–µ—Ç–æ–º —Ä–µ–≥–∏—Å—Ç—Ä–∞ (—Å–æ—Ö—Ä–∞–Ω—è–µ–º –∏—Å—Ö–æ–¥–Ω—ã–π —Ä–µ–≥–∏—Å—Ç—Ä —Å—Ç—Ä–æ–∫–∏)
            pattern = re.compile(re.escape(key), re.IGNORECASE)
            query = pattern.sub(SYNONYM_DICT[key], query, 1)
            break
    
    return query

def get_abbreviation_expansion(abbreviation: str) -> str:
    """
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ä–∞—Å—à–∏—Ñ—Ä–æ–≤–∫—É –∞–±–±—Ä–µ–≤–∏–∞—Ç—É—Ä—ã
    """
    return SYNONYM_DICT.get(abbreviation.lower(), abbreviation)

