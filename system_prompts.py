def get_prompt(prompt_key: str) -> str:
    """Получить системный промпт по ключу"""
    return SYSTEM_PROMPTS.get(prompt_key, SYSTEM_PROMPTS["general_assistant"])["prompt"]

def list_available_prompts() -> dict:
    """Получить список доступных промптов"""
    return {key: info["name"] for key, info in SYSTEM_PROMPTS.items()}

def get_prompt_name(prompt_key: str) -> str:
    """Получить название промпта по ключу"""
    return SYSTEM_PROMPTS.get(prompt_key, SYSTEM_PROMPTS["general_assistant"])["name"]